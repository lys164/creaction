def _persona_seed_samples(lang: str) -> tuple[str, str]:
    block = _PERSONA_SEED_DATA.get(lang) or _PERSONA_SEED_DATA.get("zh") or {}
    occs = block.get("occupations") or []
    surs = block.get("surnames") or []
    sep = ", " if lang == "en" else "、"
    occ = sep.join(random.sample(occs, min(6, len(occs)))) if occs else ""
    sur = " / ".join(random.sample(surs, min(8, len(surs)))) if surs else ""
    return occ, sur


